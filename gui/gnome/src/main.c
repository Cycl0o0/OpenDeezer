/* DZAccountJSON is a cheap cached read (no network), so it is safe to call on
 * the main thread right after login. Stores tier + entitlements on APP. */
static void load_account(App *a) {
  g_clear_pointer(&a->acct_name, g_free);
  g_clear_pointer(&a->acct_offer, g_free);
  a->can_hq = a->can_hifi = a->premium = FALSE;

  char *j = DZAccountJSON();
  if (!j) return;
  JsonParser *p = json_parser_new();
  if (json_parser_load_from_data(p, j, -1, NULL)) {
    JsonNode *root = json_parser_get_root(p);
    if (root && JSON_NODE_HOLDS_OBJECT(root)) {
      JsonObject *o = json_node_get_object(root);
      if (jbool(o, "loggedIn")) {
        a->acct_name = jstr(o, "name");
        a->acct_offer = jstr(o, "offer");
        a->can_hq = jbool(o, "canHq");
        a->can_hifi = jbool(o, "canHifi");
        a->premium = jbool(o, "premium");
      }
    }
  }
  g_object_unref(p);
  DZFree(j);

  if (a->acct_name && *a->acct_name) {
    toastf(a, "%s · %s", a->acct_name,
           (a->acct_offer && *a->acct_offer) ? a->acct_offer : "Deezer");
    if ((a->quality >= 2 && !a->can_hifi) || (a->quality >= 1 && !a->can_hq))
      toast(a, "Selected quality is above your plan — Deezer may stream lower");
  }
}

/* DZInit blocks on the network, so it runs on a worker. `persist` is set for the
 * login-window path (webview capture / manual entry): on success the working ARL
 * is written to arl.txt so the next launch auto-logs-in. */
typedef struct { char *arl; gboolean persist; } InitCtx;
static void init_ctx_free(gpointer p) { InitCtx *c = p; g_free(c->arl); g_free(c); }

static void init_worker(GTask *task, gpointer src, gpointer data, GCancellable *c) {
  (void)src; (void)c;
  InitCtx *ic = data;
  g_task_return_boolean(task, DZInit(ic->arl) == 1);
}
static void init_done(GObject *src, GAsyncResult *res, gpointer data) {
  (void)src; (void)data;
  InitCtx *ic = g_task_get_task_data(G_TASK(res)); /* alive until the task finalizes */
  if (g_task_propagate_boolean(G_TASK(res), NULL)) {
    if (ic && ic->persist) save_arl(ic->arl);  /* remember the working ARL for next launch */
    login_dismiss(APP);                         /* tear down the login window if it is open */
    DZSetQuality(APP->quality);                /* apply persisted quality once logged in */
    DZSetReplayGain(APP->replaygain ? 1 : 0);  /* apply persisted ReplayGain */
    DZSetGapless(APP->gapless ? 1 : 0);        /* apply persisted gapless */
    DZSetCrossfadeMS(APP->crossfade_ms);       /* apply persisted crossfade */
    if (APP->audio_device && *APP->audio_device)
      DZSetAudioDevice(APP->audio_device);     /* restore the chosen output device */
    load_account(APP);                         /* tier + entitlements (toasts name · offer) */
    /* Free accounts can't stream on-demand — gate the whole app behind a
     * blocking message (Premium required). Reached by every login path,
     * including manual-ARL / account switching. */
    if (!APP->premium) {
      show_free_block(APP);
      return;
    }
    show_main_content(APP);                     /* restore UI if returning from the block */
    if (!(APP->acct_name && *APP->acct_name))
      toast(APP, "Connected to Deezer");       /* fallback when account info is unavailable */
    /* refresh (not just append) so switching accounts replaces the previous
     * account's playlists instead of stacking the new ones underneath */
    sidebar_refresh_playlists(APP);
    /* select Liked Songs (row 0) → triggers the favorites load */
    GtkListBoxRow *row = gtk_list_box_get_row_at_index(APP->sidebar, 0);
    gtk_list_box_select_row(APP->sidebar, row);
  } else if (ic && ic->persist) {
    /* login-window path: let the user retry or fall back to manual entry */
    APP->login_busy = FALSE;
    APP->login_captured = FALSE;
    login_show_choose(APP);
    if (APP->login_status)
      gtk_label_set_label(GTK_LABEL(APP->login_status), "Login failed — please try again.");
    toast(APP, "Login failed — try again");
  } else {
    toast(APP, "Login failed — check your ARL");
  }
}

/* Kick off DZInit on a worker. Takes ownership of `arl`. */
static void start_init(App *a, char *arl, gboolean persist) {
  (void)a;
  InitCtx *ic = g_new0(InitCtx, 1);
  ic->arl = arl; /* owned */
  ic->persist = persist;
  GTask *t = g_task_new(NULL, NULL, init_done, NULL);
  g_task_set_task_data(t, ic, init_ctx_free);
  g_task_run_in_thread(t, init_worker);
  g_object_unref(t);
}

