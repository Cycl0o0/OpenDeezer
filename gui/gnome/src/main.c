/* ---------------------------------------------------------------------------
 * 300 ms poll: scrubber position, transport icon, natural-finish auto-advance
 * ------------------------------------------------------------------------- */

static gboolean tick(gpointer data) {
  App *a = data;
  int state = DZState(); /* 0 stopped,1 loading,2 playing,3 paused,4 error */
  gtk_button_set_icon_name(
      a->play_btn, state == 2 ? "media-playback-pause-symbolic" : "media-playback-start-symbolic");

  gint64 pos = DZPositionMS();
  gint64 dur = DZDurationMS();
  if (dur > 0 && !a->seeking) {
    gtk_range_set_range(GTK_RANGE(a->seek), 0, (double)dur);
    gtk_range_set_value(GTK_RANGE(a->seek), (double)pos);
  }
  char *pt = ms_to_text(pos), *dt = ms_to_text(dur);
  gtk_label_set_label(a->pos_label, pt);
  gtk_label_set_label(a->dur_label, dt);
  g_free(pt); g_free(dt);

  /* show the actual output format (e.g. "FLAC · lossless") next to the artist */
  if (a->current_index >= 0) {
    DzTrack *ct = g_list_model_get_item(G_LIST_MODEL(a->track_store), a->current_index);
    if (ct) {
      char *fmt = DZFormat(); /* labeled, malloc'd */
      if (fmt && *fmt) {
        char *sub = g_strconcat(ct->artist, "   ·   ", fmt, NULL);
        gtk_label_set_label(a->np_subtitle, sub);
        g_free(sub);
      } else {
        gtk_label_set_label(a->np_subtitle, ct->artist);
      }
      if (fmt) DZFree(fmt);
      g_object_unref(ct);
    }
  }

  /* mirror the playback status onto the OS media controls whenever it flips */
  const char *st = state == 2 ? "Playing" : (state == 3 ? "Paused" : "Stopped");
  if (g_strcmp0(st, a->mpris_status) != 0) {
    g_free(a->mpris_status);
    a->mpris_status = g_strdup(st);
    mpris_notify_status(a, st);
  }

  int fin = DZFinishedCount(); /* monotonic, +1 when a track ends naturally */
  if (fin != a->last_finished) {
    a->last_finished = fin;
    if (a->playing_episode) {
      a->playing_episode = FALSE; /* episodes don't auto-advance the track queue */
    } else if (a->gapless && DZState() == 2) {
      /* the engine already gaplessly swapped to the preloaded next track:
       * advance the UI pointer without a fresh DZPlay and preload the new next */
      advance_pointer_gapless(a);
    } else {
      play_relative(a, 1); /* auto-advance */
    }
  }

  /* keep the lyrics view in step: refetch when the track changes, then advance
   * the synced highlight to the current playback position. */
  if (a->lyrics_dialog) {
    DzTrack *ct = mpris_current_track(a);
    const char *cid = ct ? ct->id : "";
    if (g_strcmp0(cid, a->lyrics_req_id ? a->lyrics_req_id : "") != 0) {
      if (ct && a->lyrics_titlew)
        adw_window_title_set_subtitle(ADW_WINDOW_TITLE(a->lyrics_titlew), ct->name);
      lyrics_load(a, cid);
    }
    if (ct) g_object_unref(ct);
    lyrics_highlight(a);
  }

  return G_SOURCE_CONTINUE;
}

/* ---------------------------------------------------------------------------
 * Deezer login: an embedded WebKitWebView pointing at the Deezer web login.
 *
 * After the user signs in, the arl cookie (name "arl", domain .deezer.com)
 * appears in the WebView's WebKitNetworkSession cookie store. We watch that
 * store (the "changed" signal plus a 1 s poll fallback), capture the value, and
 * hand it to start_init() — which signs in, persists arl.txt, and dismisses the
 * window. A manual-ARL entry stays on the same screen as a fallback.
 * ------------------------------------------------------------------------- */

/* Persist the working ARL to the SAME file the frontend reads at startup
 * (<UserConfigDir>/opendeezer/arl.txt) so the next launch auto-logs-in. */
static void save_arl(const char *arl) {
  if (!arl || !*arl) return;
  char *dir = g_build_filename(g_get_user_config_dir(), "opendeezer", NULL);
  g_mkdir_with_parents(dir, 0700);
  char *path = g_build_filename(dir, "arl.txt", NULL);
  g_file_set_contents(path, arl, -1, NULL); /* best effort */
  g_free(path);
  g_free(dir);
}

/* Stop the cookie poll and drop borrowed widget/manager pointers (the widgets
 * themselves are owned by the window and freed when it is destroyed). */
static void login_cleanup(App *a) {
  if (a->login_poll_id) { g_source_remove(a->login_poll_id); a->login_poll_id = 0; }
  a->login_cm = NULL;          /* owned by the network session */
  a->login_web = NULL;         /* owned by the stack */
  a->login_stack = NULL;
  a->login_back = NULL;
  a->login_status = NULL;
  a->login_manual_entry = NULL;
}

/* Close + free the login window (success path). */
static void login_dismiss(App *a) {
  if (!a->login_win) return;
  login_cleanup(a);
  GtkWindow *w = a->login_win;
  a->login_win = NULL;
  gtk_window_destroy(w); /* does NOT re-emit close-request */
}

static gboolean on_login_close(GtkWindow *win, gpointer data) {
  (void)win;
  App *a = data;
  login_cleanup(a);
  a->login_win = NULL;
  return FALSE; /* allow the default destroy */
}

/* Switch back to the choose page and stop polling. */
static void login_show_choose(App *a) {
  if (a->login_poll_id) { g_source_remove(a->login_poll_id); a->login_poll_id = 0; }
  if (a->login_stack) gtk_stack_set_visible_child_name(a->login_stack, "choose");
  if (a->login_back) gtk_widget_set_visible(a->login_back, FALSE);
}

/* The cookie store reported an arl cookie — extract it and sign in. */
static void on_login_cookies(GObject *src, GAsyncResult *res, gpointer data) {
  App *a = data;
  GError *e = NULL;
  GList *cookies =
      webkit_cookie_manager_get_cookies_finish(WEBKIT_COOKIE_MANAGER(src), res, &e);
  if (e) { g_clear_error(&e); return; }

  char *arl = NULL;
  for (GList *l = cookies; l; l = l->next) {
    SoupCookie *c = l->data;
    if (g_strcmp0(soup_cookie_get_name(c), "arl") == 0) {
      const char *v = soup_cookie_get_value(c);
      if (v && *v) { g_free(arl); arl = g_strdup(v); } /* keep the latest non-empty */
    }
  }
  g_list_free_full(cookies, (GDestroyNotify)soup_cookie_free);

  if (!arl) return; /* not logged in yet */
  if (a->login_captured || a->login_busy) { g_free(arl); return; }

  a->login_captured = TRUE; /* stop further polling for this attempt */
  a->login_busy = TRUE;
  if (a->login_poll_id) { g_source_remove(a->login_poll_id); a->login_poll_id = 0; }
  if (a->login_status) gtk_label_set_label(GTK_LABEL(a->login_status), "Signing in…");
  start_init(a, arl, TRUE); /* owns arl; persists + dismisses on success */
}

/* Ask the cookie store for deezer.com's cookies (async). */
static void login_check_cookies(App *a) {
  if (!a->login_cm || a->login_captured || a->login_busy) return;
  webkit_cookie_manager_get_cookies(a->login_cm, "https://www.deezer.com",
                                    NULL, on_login_cookies, a);
}

static void on_cookies_changed(WebKitCookieManager *cm, gpointer data) {
  (void)cm;
  login_check_cookies((App *)data);
}

static gboolean login_poll(gpointer data) {
  login_check_cookies((App *)data);
  return G_SOURCE_CONTINUE;
}

/* Build (once) the webview, then show it and start watching cookies. */
static void login_show_web(App *a) {
  if (!a->login_web) {
    GtkWidget *web = webkit_web_view_new();
    a->login_web = WEBKIT_WEB_VIEW(web);
    gtk_widget_set_hexpand(web, TRUE);
    gtk_widget_set_vexpand(web, TRUE);
    WebKitNetworkSession *sess = webkit_web_view_get_network_session(a->login_web);
    a->login_cm = webkit_network_session_get_cookie_manager(sess);
    g_signal_connect(a->login_cm, "changed", G_CALLBACK(on_cookies_changed), a);
    gtk_stack_add_named(a->login_stack, web, "web");
  }
  a->login_captured = FALSE;
  a->login_busy = FALSE;
  webkit_web_view_load_uri(a->login_web, "https://www.deezer.com/login");
  gtk_stack_set_visible_child_name(a->login_stack, "web");
  gtk_widget_set_visible(a->login_back, TRUE);
  if (!a->login_poll_id)
    a->login_poll_id = g_timeout_add(1000, login_poll, a); /* fallback to the signal */
}

static void on_login_with_deezer(GtkButton *b, gpointer data) {
  (void)b;
  login_show_web((App *)data);
}

static void on_login_back(GtkButton *b, gpointer data) {
  (void)b;
  login_show_choose((App *)data);
}

