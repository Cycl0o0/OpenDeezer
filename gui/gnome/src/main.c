static void load_css(void) {
  /* libadwaita >=1.6 reads the CSS custom properties; the @define-color block
   * is the pre-1.6 fallback. Both are harmless to the other parser. */
  static const char *css =
      ":root{"
      "  --accent-bg-color:" ACCENT ";"
      "  --accent-color:" ACCENT ";"
      "  --accent-fg-color:#ffffff;"
      "}\n"
      "@define-color accent_bg_color " ACCENT ";\n"
      "@define-color accent_color " ACCENT ";\n"
      "@define-color accent_fg_color #ffffff;\n"
      ".np-cover{border-radius:6px;}\n"
      ".dz-avatar{border-radius:9999px;}\n"
      ".dz-liked{color:" ACCENT ";}\n"
      ".lyrics-line{padding:5px 14px;opacity:0.45;}\n"
      ".lyrics-line.lyrics-active{opacity:1;font-weight:700;color:" ACCENT ";}\n"
      ".lyrics-plain{padding:8px 14px;}\n"
      /* explicit-content "E" tag: a small theme-aware boxed glyph */
      ".explicit-badge{"
      "  font-size:0.68em;"
      "  font-weight:800;"
      "  padding:0px 5px;"
      "  border-radius:4px;"
      "  background-color:alpha(currentColor,0.20);"
      "}\n";
  GtkCssProvider *p = gtk_css_provider_new();
#if GTK_CHECK_VERSION(4, 12, 0)
  gtk_css_provider_load_from_string(p, css);
#else
  gtk_css_provider_load_from_data(p, css, -1);
#endif
  gtk_style_context_add_provider_for_display(
      gdk_display_get_default(), GTK_STYLE_PROVIDER(p), GTK_STYLE_PROVIDER_PRIORITY_APPLICATION);
  g_object_unref(p);
}

