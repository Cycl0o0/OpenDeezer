/* ---------------------------------------------------------------------------
 * browse: load a track list on a worker thread, populate on the main thread
 * ------------------------------------------------------------------------- */

typedef struct { LoadKind kind; char *arg; } LoadCtx;
static void load_ctx_free(gpointer p) { LoadCtx *c = p; g_free(c->arg); g_free(c); }

static void load_worker(GTask *task, gpointer src, gpointer data, GCancellable *c) {
  (void)src; (void)c;
  LoadCtx *ctx = data;
  char *j = NULL;
  switch (ctx->kind) {
    case LOAD_FAVORITES: j = DZFavoritesJSON(); break;
    case LOAD_PLAYLIST:  j = DZPlaylistTracksJSON(ctx->arg); break;
    case LOAD_ALBUM:     j = DZAlbumTracksJSON(ctx->arg); break;
    case LOAD_SEARCH:    j = DZSearchJSON(ctx->arg); break;
    case LOAD_CHARTS:    j = DZChartsJSON(); break;
    case LOAD_FLOW:      j = DZFlowJSON(); break;
  }
  char *dup = g_strdup(j ? j : "{}");
  if (j) DZFree(j);
  g_task_return_pointer(task, dup, g_free);
}

/* fills the column store from a {"tracks":[...]} (or search) JSON payload */
static void populate_tracks(App *a, const char *json) {
  g_list_store_remove_all(a->track_store);
  a->current_index = -1;
  if (!json) return;

  JsonParser *p = json_parser_new();
  GError *e = NULL;
  if (!json_parser_load_from_data(p, json, -1, &e)) {
    toastf(a, "Couldn't parse response: %s", e->message);
    g_clear_error(&e);
    g_object_unref(p);
    return;
  }
  JsonNode *root = json_parser_get_root(p);
  if (root && JSON_NODE_HOLDS_OBJECT(root)) {
    JsonObject *o = json_node_get_object(root);
    if (json_object_has_member(o, "error")) {
      char *msg = jstr(o, "error");
      if (*msg) toastf(a, "Deezer: %s", msg);
      g_free(msg);
    }
    if (json_object_has_member(o, "tracks")) {
      JsonArray *arr = json_object_get_array_member(o, "tracks");
      guint n = json_array_get_length(arr);
      for (guint i = 0; i < n; i++) {
        DzTrack *t = dz_track_from_json(json_array_get_object_element(arr, i));
        g_list_store_append(a->track_store, t);
        g_object_unref(t);
      }
      toastf(a, n == 1 ? "%u track" : "%u tracks", n);
    }
  }
  g_object_unref(p);
}

static void load_done(GObject *src, GAsyncResult *res, gpointer data) {
  (void)src; (void)data;
  LoadCtx *c = g_task_get_task_data(G_TASK(res)); /* alive until the task finalizes */
  char *json = g_task_propagate_pointer(G_TASK(res), NULL);
  if (c && (c->kind == LOAD_SEARCH || c->kind == LOAD_CHARTS)) {
    populate_browse(APP, json); /* sectioned: tracks/albums/artists/playlists */
    show_browse(APP);
  } else {
    populate_tracks(APP, json);
    show_tracks(APP);
    if (c && c->kind == LOAD_FLOW &&
        g_list_model_get_n_items(G_LIST_MODEL(APP->track_store)) > 0)
      play_index(APP, 0); /* Flow auto-plays */
  }
  g_free(json);
}

static void load_async(App *a, LoadKind kind, const char *arg) {
  LoadCtx *c = g_new0(LoadCtx, 1);
  c->kind = kind;
  c->arg = g_strdup(arg ? arg : "");
  GTask *t = g_task_new(NULL, NULL, load_done, NULL);
  g_task_set_task_data(t, c, load_ctx_free);
  g_task_run_in_thread(t, load_worker);
  g_object_unref(t);
}

