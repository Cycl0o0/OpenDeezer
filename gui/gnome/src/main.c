/*
 * OpenDeezer — native GNOME front-end (GTK4 + libadwaita).
 *
 * The entire engine (login, browse, Blowfish decrypt, MP3 decode, ALSA
 * playback) is the Go core compiled to a C static archive
 * (lib/libdeezercore.a) and linked in-process; this file is UI only.
 *
 * Layout:  AdwApplicationWindow > AdwNavigationSplitView
 *            sidebar  : Liked Songs + the user's playlists (GtkListBox)
 *            content  : a GtkColumnView track table + a now-playing bottom bar,
 *                       with a search entry in the header.
 *
 * Threading: every DZ* call that touches the network (DZInit, the *JSON browse
 * calls, DZPlay, DZFetch) BLOCKS, so it runs on a GTask worker thread and the
 * result is marshalled back to the main loop in the completion callback. Cheap
 * state reads (DZState/DZPositionMS/...) are polled directly from a 300 ms
 * g_timeout on the main thread.
 */

